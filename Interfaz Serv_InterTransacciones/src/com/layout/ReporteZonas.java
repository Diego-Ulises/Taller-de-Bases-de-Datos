    private void jButtonBuscarZonasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarZonasActionPerformed
        try {
            String empresa = jComboBoxEmpresas.getSelectedItem().toString();
            String dataf = jComboBoxFiltro.getSelectedItem().toString();
            if (jRadioButtonNinguno.isSelected()) {
                
            } else if (jRadioButtonCiudad.isSelected()) {
                allZonas = handler.showRepZonasFiltroCiudad(empresa,dataf);
                updateTable();
            } else if (jRadioButtonEstado.isSelected()) {
                allZonas = handler.showRepZonasFiltroEstado(empresa,dataf);
                updateTable();
            } else if (jRadioButtonNomComer.isSelected()) {
                allZonas = handler.showRepZonasFiltroNomComer(empresa,dataf);
                updateTable();
            }
        } catch (Exception e) {
                allZonas = handler.showRepAllZonas();
                updateTable();
        }
    }//GEN-LAST:event_jButtonBuscarZonasActionPerformed

