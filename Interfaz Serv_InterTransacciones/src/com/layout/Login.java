    private void jButtonAccesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAccesoActionPerformed
        String password = new String(jPasswordField.getPassword());
        String user = jTextFieldUsuario.getText();
        if (user.equalsIgnoreCase("Registro") && password.equalsIgnoreCase("12345")) {
            this.setVisible(false);
            new Registro().setVisible(true);
        } else {
            //password = "germany10";
            int tipoUsuario = primerConexion.Login(user);
            int idRegistro = primerConexion.IdReg(user);
            if (password.equals("germany10") && tipoUsuario != 0) {
                String dateTime = getNow().toString();
                try {
                    primerConexion.LoginUsuario(user, dateTime, dateTime, idRegistro);
                } catch (SQLException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
                this.setVisible(false);
                new Principal(tipoUsuario).setVisible(true);
             
                Principal.Logs(tipoUsuario, idRegistro);
            } else {
                JOptionPane.showMessageDialog(null, "Verifica tus datos!");
            }
        }
    }//GEN-LAST:event_jButtonAccesoActionPerformed

