    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        try {
            String dataf = jComboBoxFiltro.getSelectedItem().toString();
            if (jRadioButtonNinguno.isSelected()) {
                allContratos = handler.showReportViewContratosGral();
                updateTable();
                
            } else if (jRadioButtonLocZona.isSelected()) {
                allContratos = handler.showRepZonasFiltroLocZona(dataf);
                updateTable();
            } else if (jRadioButtonStatus.isSelected()) {
                allContratos = handler.showRepZonasFiltroStatus(dataf);
                updateTable();
            } else if (jRadioButtonNomComer.isSelected()) {
                allContratos = handler.showRepZonasFiltroNomComer(dataf);
                updateTable();
            } else if (jRadioButtonNoContrato.isSelected()) {
                allContratos = handler.showRepZonasFiltroNoContrato(dataf);
                updateTable();
            }else if (jRadioButtonEmpresa.isSelected()) {
                allContratos = handler.showRepZonasFiltroEmpresa(dataf);
                updateTable();
            }else if (jRadioButtonTipoServ.isSelected()) {
                allContratos = handler.showRepZonasFiltroTipoServ(dataf);
                updateTable();
            }
        } catch (Exception e) {
               allContratos = handler.showReportViewContratosGral();
                updateTable(); 
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

