    private void updateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        for (int i = (model.getRowCount()); i < allContratos.size(); i++) {
            cont = cont+1;
            ClaseReportAllContratos tmp = allContratos.get(i);
            Object[] rowData = new Object[]{tmp.getNoContrato(), tmp.getStatus(), tmp.getNombreEmpresa(), tmp.getNombreComercial(), tmp.getLocalizacionZona(), tmp.getTipoServicio(), tmp.getVelDescarga(),tmp.getVelSubida(), tmp.getNoSerie(), tmp.getFecha(), tmp.getIdMovimiento()};
            model.addRow(rowData);
        }
        jTable1.updateUI();
        String cad;
        cad = Integer.toString(cont);
        jTextField1.setText(cad);
        cont = 0;
    }
